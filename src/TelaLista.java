import javax.swing.*;

public class TelaLista {
	private JPanel conteudo;
	private JButton voltarBotao;
	private JTabbedPane tabs;
	private JButton vacinaButton;
	private JButton pesoButton;
	private JButton castrarButton;

	public JPanel getContent() {
		return conteudo;
	}

	public JButton getVoltarBotao() {
		return voltarBotao;
	}

	public JTabbedPane getTabs() {
		return tabs;
	}


	public JButton getVacinaButton() {
		return vacinaButton;
	}

	public JButton getPesoButton() {
		return pesoButton;
	}

	public JButton getCastrarButton() {
		return castrarButton;
	}
}
